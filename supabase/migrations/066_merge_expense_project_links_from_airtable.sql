-- ============================================================
-- Merge expense→project links from Airtable's real "Projects" linked-
-- record field on the Expense table (fldMXUlngdf4BolcM), not the
-- free-text "Project Name" field.
--
-- Migration 065 backfilled project_id by matching the free-text
-- project_name column against projects.project_name, but that field is
-- frequently blank in Airtable even when the Expense record genuinely
-- has a project linked via the real relational field (confirmed by
-- inspecting the live KUNCH_11 base — see the "Projects" card on an
-- Expense record, which shows Department / Project Manager / Expense
-- Tracker even when "Project Name" underneath is empty). That left far
-- more expenses without a project link than actually exist in Airtable.
--
-- This pulls the authoritative expense_code -> project_name pairs
-- straight from a live read of the Expense table's Projects link field
-- (2,381 unique expense codes had a real project link; 1 was skipped
-- for having two different linked project names across duplicate
-- Airtable rows sharing the same formula-generated Expense Code, and
-- needs manual review) and applies them by matching expense_code
-- (exact) and project_name (exact, case-insensitive/trimmed) — no fuzzy
-- text guessing.
--
-- A handful of expenses genuinely link to more than one project in
-- Airtable (92 records); expenses.project_id is a single-value column,
-- so — same as the original migration script's linkedId() helper — the
-- first linked project is used as the primary one.
--
-- Safe to re-run: only touches rows where project_id IS NULL.
--
-- This file is ~120KB, which can get silently truncated when copy-pasted
-- through a browser into the Supabase SQL Editor (this happened on this
-- exact file — "relation _exp_project_import does not exist" means the
-- paste was cut before the CREATE TABLE line ever ran). Use the
-- pre-split files in ./066_parts/ instead: run 066_part00_create_table.sql,
-- then 066_part01.sql through 066_part08.sql in order, then
-- 066_part09_apply.sql last. Each part is small enough to paste reliably.
-- ============================================================

